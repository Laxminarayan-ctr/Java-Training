package JavaBasic;

public class IncrementDecrementOperator {
	
	public static void main(String[] arg) {
		
	 int i = 1;
	 System.out.println(i);
	 int j = i++; //Post increment
	 System.out.println(i);
	 System.out.println(j);
	 
	 
	 System.out.println("*****************");
	 
	 
	 int n = 2;
	 int k = n--;//Post decrement
	 System.out.println(n);
	 System.out.println(k);
	}

}
