package OOPConceptPart2;

public class A {
	
	public A()
	{
		System.out.println("this is parent class Constructor");
	}
	public A(int i)
	{
		System.out.println("this is parent class Constructor for i=" +i);
	}
	public A(int i,int k)
	{
		System.out.println("the value of i and k is = "+i);
		System.out.println("the value of i and k is = "+k);
	}
}
